fn fallback_symbol_template(market: &str, idx: usize) -> (&'static str, &'static str) {
    let fallback = match market.to_uppercase().as_str() {
        "US" => format!("US{:03}", idx),
        "A" => format!("{:06}", 600000 + idx as i32),
        "JP" => format!("{:04}", 7000 + idx as i32),
        _ => format!("SYM{:03}", idx),
    };
    let leaked = Box::leak(fallback.into_boxed_str());
    (leaked, "IND0")
}

