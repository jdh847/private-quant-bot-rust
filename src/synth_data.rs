use crate::calendar::ExchangeCalendar;

#[derive(Debug, Clone)]
pub struct SynthDatasetRequest {
    pub output_dir: PathBuf,
    pub start_date: NaiveDate,
    pub end_date: NaiveDate,
    pub seed: u64,
    pub us_symbols: usize,
    pub a_symbols: usize,
    pub jp_symbols: usize,
    pub industries_per_market: usize,
    pub force: bool,
}

pub fn generate_synth_dataset(req: &SynthDatasetRequest) -> Result<()> {
    if req.start_date > req.end_date {
        return Err(anyhow!("start_date must be <= end_date"));
    }
    if req.industries_per_market == 0 {
        return Err(anyhow!("industries_per_market must be > 0"));
    }
    if req.us_symbols == 0 || req.a_symbols == 0 || req.jp_symbols == 0 {
        return Err(anyhow!("symbol counts must be > 0 for each market"));
    }

    let out = &req.output_dir;
    fs::create_dir_all(out)?;

    let files = [
        out.join("us_equities.csv"),
        out.join("a_share.csv"),
        out.join("jp_equities.csv"),
        out.join("us_industries.csv"),
        out.join("a_industries.csv"),
        out.join("jp_industries.csv"),
    ];
    if !req.force {
        for f in &files {
            if f.exists() {
                return Err(anyhow!(
                    "refusing to overwrite existing file (pass --force): {}",
                    f.display()
                ));
            }
        }
    }

    let cal = ExchangeCalendar::new();
    let mut rng = SplitMix64::new(req.seed);

    let specs = [
        MarketSpec {
            market: "US",
            symbols: req.us_symbols,
            industries: req.industries_per_market,
            bars_path: out.join("us_equities.csv"),
            industries_path: out.join("us_industries.csv"),
        },
        MarketSpec {
            market: "A",
            symbols: req.a_symbols,
            industries: req.industries_per_market,
            bars_path: out.join("a_share.csv"),
            industries_path: out.join("a_industries.csv"),
        },
        MarketSpec {
            market: "JP",
            symbols: req.jp_symbols,
            industries: req.industries_per_market,
            bars_path: out.join("jp_equities.csv"),
            industries_path: out.join("jp_industries.csv"),
        },
    ];
    for spec in specs {
        generate_market(&cal, &mut rng, req.start_date, req.end_date, &spec)?;
    }

    let readme = out.join("README.md");
    fs::write(
        &readme,
        format!(
            "# Synthetic Dataset (Generated)\n\n\
Generated by `private-quant-bot gen-synth-data`.\n\n\
This dataset is still synthetic, but it is designed to be **longer** and **more market-like** than the tiny bootstrap sample used in the earliest versions of the repo.\n\n\
Characteristics:\n\
- multi-year daily history\n\
- market + industry + idiosyncratic return components\n\
- occasional zero-volume / halt-like days\n\
- optional split-like events via `adj_close`\n\
- market-specific symbol shapes and industry labels\n\n\
Params:\n\
- start_date={}\n\
- end_date={}\n\
- seed={}\n\
- us_symbols={}\n\
- a_symbols={}\n\
- jp_symbols={}\n\
- industries_per_market={}\n\n\
Files:\n\
- us_equities.csv / a_share.csv / jp_equities.csv\n\
  - schema: date,symbol,close,adj_close,volume\n\
- us_industries.csv / a_industries.csv / jp_industries.csv\n\
  - schema: symbol,industry\n",
            req.start_date,
            req.end_date,
            req.seed,
            req.us_symbols,
            req.a_symbols,
            req.jp_symbols,
            req.industries_per_market
        ),
    )?;

    Ok(())
}

#[derive(Debug, Clone)]
struct MarketSpec {
    market: &'static str,
    symbols: usize,
    industries: usize,
    bars_path: PathBuf,
    industries_path: PathBuf,
}

#[derive(Debug, Clone)]
struct SymbolProfile {
    symbol: String,
    industry: String,
    start_price: f64,
    base_volume: f64,
    market_beta: f64,
    industry_beta: f64,
    idio_vol: f64,
    drift_bias: f64,
}

#[derive(Debug, Clone, Copy)]
struct MarketDynamics {
    daily_drift: f64,
    market_vol: f64,
    industry_vol: f64,
    halt_chance: f64,
    jump_chance: f64,
    max_abs_return: f64,
    split_chance: f64,
}

fn generate_market(
    cal: &ExchangeCalendar,
    rng: &mut SplitMix64,
    start: NaiveDate,
    end: NaiveDate,
    spec: &MarketSpec,
) -> Result<()> {
    let profiles = build_symbol_profiles(spec, rng);
    write_industries(&profiles, &spec.industries_path)?;
    write_bars(
        cal,
        rng,
        spec.market,
        &profiles,
        start,
        end,
        &spec.bars_path,
    )?;
    Ok(())
}

fn build_symbol_profiles(spec: &MarketSpec, rng: &mut SplitMix64) -> Vec<SymbolProfile> {
    let templates = market_symbol_templates(spec.market);
    let target_industries = spec.industries.max(1);

    (0..spec.symbols)
        .map(|idx| {
            let fallback = fallback_symbol_template(spec.market, idx + 1);
            let template = templates.get(idx).unwrap_or(&fallback);
            let industry = if idx < templates.len() {
                template.1.to_string()
            } else {
                format!("IND{}", idx % target_industries)
            };
            let (start_price, base_volume) = market_price_volume_seed(spec.market, idx, rng);
            SymbolProfile {
                symbol: template.0.to_string(),
                industry,
                start_price,
                base_volume,
                market_beta: 0.8 + 0.5 * rng.next_f64(),
                industry_beta: 0.5 + 0.4 * rng.next_f64(),
                idio_vol: market_idio_vol(spec.market) * (0.8 + 0.5 * rng.next_f64()),
                drift_bias: (rng.next_f64() - 0.5) * market_idio_vol(spec.market) * 0.18,
            }
        })
        .collect()
}

fn write_industries(profiles: &[SymbolProfile], path: &Path) -> Result<()> {
    let mut wtr = csv::Writer::from_path(path)
        .with_context(|| format!("open industries output failed: {}", path.display()))?;
    wtr.write_record(["symbol", "industry"])?;
    for profile in profiles {
        wtr.write_record([profile.symbol.as_str(), profile.industry.as_str()])?;
    }
    wtr.flush()?;
    Ok(())
}

fn write_bars(
    cal: &ExchangeCalendar,
    rng: &mut SplitMix64,
    market: &str,
    profiles: &[SymbolProfile],
    start: NaiveDate,
    end: NaiveDate,
    path: &Path,
) -> Result<()> {
    let mut wtr = csv::Writer::from_path(path)
        .with_context(|| format!("open bars output failed: {}", path.display()))?;
    wtr.write_record(["date", "symbol", "close", "adj_close", "volume"])?;

    let mut price = profiles
        .iter()
        .map(|profile| (profile.symbol.clone(), profile.start_price))
        .collect::<HashMap<_, _>>();
    let mut adj_factor = profiles
        .iter()
        .map(|profile| (profile.symbol.clone(), 1.0))
        .collect::<HashMap<_, _>>();
    let mut industry_state = profiles
        .iter()
        .map(|profile| (profile.industry.clone(), 0.0))
        .collect::<HashMap<_, _>>();
    let dynamics = market_dynamics(market);
    let mut market_state = 0.0;

    let mut d = start;
    while d <= end {
        if !cal.is_trading_day(market, d) {
            d += Duration::days(1);
            continue;
        }

        market_state = 0.88 * market_state + dynamics.market_vol * 0.35 * rng.normal();
        if rng.next_f64() < dynamics.jump_chance {
            market_state +=
                dynamics.market_vol * 1.8 * if rng.next_f64() < 0.5 { -1.0 } else { 1.0 };
        }

        for state in industry_state.values_mut() {
            *state = 0.82 * *state + dynamics.industry_vol * rng.normal();
        }

        for profile in profiles {
            let p0 = price
                .get(&profile.symbol)
                .copied()
                .unwrap_or(profile.start_price)
                .max(0.5);
            let industry_component = industry_state
                .get(&profile.industry)
                .copied()
                .unwrap_or(0.0);
            let mut ret = dynamics.daily_drift
                + market_state * profile.market_beta
                + industry_component * profile.industry_beta
                + profile.drift_bias
                + profile.idio_vol * rng.normal();
            if rng.next_f64() < dynamics.jump_chance * 0.8 {
                ret += profile.idio_vol * 2.2 * if rng.next_f64() < 0.45 { -1.0 } else { 1.0 };
            }
            ret = ret.clamp(-dynamics.max_abs_return, dynamics.max_abs_return);

            let mut raw_close = (p0 * (1.0 + ret)).max(0.5);
            let mut volume =
                (profile.base_volume * (1.0 + ret.abs() * 18.0) * (0.75 + 0.5 * rng.next_f64()))
                    .max(0.0);
            let mut factor = adj_factor.get(&profile.symbol).copied().unwrap_or(1.0);

            if rng.next_f64() < dynamics.halt_chance {
                raw_close = p0;
                volume = 0.0;
            }

            if rng.next_f64() < dynamics.split_chance {
                raw_close *= 0.5;
                factor *= 2.0;
            }

            price.insert(profile.symbol.clone(), raw_close);
            adj_factor.insert(profile.symbol.clone(), factor);

            let close = round_to(raw_close, 4);
            let adj_close = round_to(raw_close * factor, 4);
            let volume = round_to(volume, 2);

            wtr.write_record([
                d.format("%Y-%m-%d").to_string(),
                profile.symbol.clone(),
                close.to_string(),
                adj_close.to_string(),
                volume.to_string(),
            ])?;
        }

        d += Duration::days(1);
    }
    wtr.flush()?;
    Ok(())
}

